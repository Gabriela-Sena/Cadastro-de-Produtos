    private void jBGravarArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGravarArquivoActionPerformed
        // TODO add your handling code here:
        
    if(caminho.length()>0){
            try{
                arquivo = new File(caminho);
                arquivo.createNewFile();
                JOptionPane.showMessageDialog(null, "Arquivo salvo com sucesso!");
            }
            catch(IOException e){
                System.err.println(e);
            }
            
            FileWriter escrita = null;
            
        try {
            escrita = new FileWriter(arquivo);
        } catch (IOException ex) {
            Logger.getLogger(jFTabelaRegistro.class.getName()).log(Level.SEVERE, null, ex);
        }
             
            PrintWriter escritaFormatada = new PrintWriter(escrita);
            
            for(int linha = 0; linha < tabelaproduto.getRowCount(); linha++ ){
                Produto produto = tabelaproduto.getProduto(linha); 
                
                int cod = produto.getCod();
                String nomeProduto = produto.getNomeProduto();
                int quantidade = produto.getQuantidade();
                double preco = produto.getPreco();
                String tipo = produto.getTipo();
                
                escritaFormatada.printf("%d / %s / %d / %.2f / %s %n", cod, nomeProduto, quantidade, preco,tipo);
            }
            
            try {
                escrita.close();
            } catch (IOException ex) {
                Logger.getLogger(jFTabelaRegistro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jBGravarArquivoActionPerformed

    private void jBLerArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBLerArquivoActionPerformed
        // TODO add your handling code here:
        
        Scanner leitura = null;
        
        if(caminho.length() > 0){
            try {
                leitura = new Scanner(Paths.get(caminho));
                
            } catch (IOException ex) {
                Logger.getLogger(jFTabelaRegistro.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            while(leitura.hasNextLine()){
                
                String linha = leitura.nextLine();
                System.out.println(linha);
                
                String[] palavras = linha.split(" / "); //delimita as plavras em cada posicao pela /
                
                int cod= Integer.parseInt(palavras[0]);
                String nomeProduto = palavras[1];
                int quantidade= Integer.parseInt(palavras[2]);
                double preco = Double.parseDouble(palavras[3].replace(',','.'));
                String tipo = palavras[4];
        
                Produto produto = new Produto(cod, nomeProduto, quantidade, preco,tipo);
        
                tabelaproduto.adicionarProduto(produto);
            }
            JOptionPane.showMessageDialog(null, "Arquivo lido com sucesso!");
        }
    }//GEN-LAST:event_jBLerArquivoActionPerformed

