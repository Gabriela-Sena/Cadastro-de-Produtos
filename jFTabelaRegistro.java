    private void jBRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRemoverActionPerformed
        // TODO add your handling code here:
        int linha = jTProdutos.getSelectedRow();
        if(linha != -1){
            try {
                SQLConexao.deletarRegistro((int)tabelaproduto.getValueAt(linha,0));
                tabelaproduto.removerProduto(linha);
                
            } catch (SQLException ex) {
                Logger.getLogger(jFTabelaRegistro.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um produto!");
        }

    }//GEN-LAST:event_jBRemoverActionPerformed

    private void jBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarActionPerformed
        // TODO add your handling code here:
        if(jTProdutos.getSelectedRow() != -1){
            
            String nomeProduto = jTNomeProduto.getText();
            int cod = Integer.parseInt(jTCod.getText());
            int quantidade = Integer.parseInt(jTQuantidade.getText());
            double preco = Double.parseDouble(jTPreço.getText());
            String tipo= jTTipo.getText();
            
            try {
                SQLConexao.alterarRegistro(nomeProduto, quantidade, preco,tipo, cod);
            } catch (SQLException ex) {
                Logger.getLogger(jFTabelaRegistro.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            Produto produto = new Produto(cod,nomeProduto ,quantidade, preco,tipo);
            
            tabelaproduto.alterarProduto(produto, jTProdutos.getSelectedRow());
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um produto!");
        }
        limparCampos();
        
    }//GEN-LAST:event_jBAlterarActionPerformed

